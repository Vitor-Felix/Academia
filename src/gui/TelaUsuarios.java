    private void carregarJTable() {
        ArrayList<Clientes> lista = RegistradorClientes.getInstance().obterListaClientes();

        DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("Matricula");
        modelo.addColumn("Nome");
        modelo.addColumn("Idade");
        modelo.addColumn("Sexo");
        modelo.addColumn("Peso");

        if (lista.size() == 0) {
            modelo.addRow(new String[]{"Sem dados",
                        null,
                        null,
                        null,
                        null});
        }
        
        for (int i = 0; i < lista.size(); i++) {
            Clientes c = lista.get(i);
            System.out.println(c.toString());

            // Alimenta as linhas de dados  
            modelo.addRow(
                    new String[]{
                        c.getMatricula() + "",//Talvez dÃª problema!
                        c.getNome(),
                        (2015 - c.getAno()) + "",
                        c.isSexo() == false ? "Masculino" : "Feminino",
                        c.getPeso() + ""
                    });
        }

        jTable1.setModel(modelo);

    }
    
    public void atualizarModelo() {
        
        System.out.println("atualizando modelo...");
        carregarJTable();
    }

